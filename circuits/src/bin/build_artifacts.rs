//! Generates the circuit artifacts under `circuits/artifacts/`:
//! proving/verifying keys for the protocol-shape circuits, the
//! host-encoded verifying keys a `zk_verifier` instance is constructed
//! with, layout manifests, and SHA-256 checksums — plus the generated
//! Poseidon constants consumed by the contract layer.
//!
//! ## DEVELOPMENT KEYS ONLY
//!
//! Keys are generated from a fixed, public RNG seed so anyone can
//! reproduce them byte-for-byte. That means the setup toxic waste is
//! public: these keys prove nothing about mainnet. The production keys
//! come from the public multi-party ceremony (`circuits/ceremony/`),
//! which replaces this seeding step; everything else in this pipeline
//! stays identical.

use ark_bls12_381::{Bls12_381, Fr};
use ark_groth16::{Groth16, ProvingKey, VerifyingKey};
use ark_serialize::CanonicalSerialize;
use ark_snark::SNARK;
use attesta_circuits::encoding::{fr_to_bytes, vk_to_bytes};
use attesta_circuits::merkle::POOL_TREE_DEPTH;
use attesta_circuits::poseidon;
use attesta_circuits::transfer::TransferCircuit;
use attesta_circuits::withdraw::WithdrawCircuit;
use rand_chacha::rand_core::SeedableRng;
use rand_chacha::ChaCha20Rng;
use std::fmt::Write as _;
use std::fs;
use std::path::Path;

/// Public dev seed — reproducibility over secrecy, see module docs.
const DEV_SEED: &[u8; 32] = b"attesta-dev-setup-not-for-prod!!";

/// Transfer arity of the protocol instance.
const N_IN: usize = 2;
const N_OUT: usize = 2;

fn main() {
    let root = Path::new(env!("CARGO_MANIFEST_DIR"));
    let artifacts = root.join("artifacts");
    fs::create_dir_all(&artifacts).unwrap();

    println!("attesta-circuits: artifact build (DEV keys, seed public)");

    let mut rng = ChaCha20Rng::from_seed(*DEV_SEED);
    let (transfer_pk, transfer_vk) = Groth16::<Bls12_381>::circuit_specific_setup(
        TransferCircuit::blank(POOL_TREE_DEPTH, N_IN, N_OUT),
        &mut rng,
    )
    .unwrap();
    emit_circuit(
        &artifacts,
        "transfer",
        &transfer_pk,
        &transfer_vk,
        &format!(
            "[root, nullifier_0..{}, new_commitment_0..{}]",
            N_IN - 1,
            N_OUT - 1
        ),
        1 + N_IN + N_OUT,
    );

    let (withdraw_pk, withdraw_vk) = Groth16::<Bls12_381>::circuit_specific_setup(
        WithdrawCircuit::blank(POOL_TREE_DEPTH),
        &mut rng,
    )
    .unwrap();
    emit_circuit(
        &artifacts,
        "withdraw",
        &withdraw_pk,
        &withdraw_vk,
        "[root, nullifier, recipient_binding, amount]",
        4,
    );

    emit_poseidon_params(root);

    fs::write(
        artifacts.join("README.md"),
        "# Circuit artifacts — DEVELOPMENT KEYS\n\n\
         Generated by `scripts/build-artifacts.sh` from a fixed public\n\
         seed: reproducible by anyone, secure for no one. The setup\n\
         randomness is public, so **these keys must never gate real\n\
         value**. Production keys come from the multi-party ceremony\n\
         described in `../ceremony/`.\n\n\
         Per circuit: `verifying_key.hex` (host-encoded fields, what a\n\
         `zk_verifier` instance is constructed with), `verifying_key.bin`\n\
         (arkworks canonical), `proving_key.bin` (arkworks canonical,\n\
         gitignored for size — regenerate with the script), `layout.txt`\n\
         (public-input order), and `SHA256SUMS`.\n",
    )
    .unwrap();

    println!("done.");
}

fn emit_circuit(
    artifacts: &Path,
    name: &str,
    pk: &ProvingKey<Bls12_381>,
    vk: &VerifyingKey<Bls12_381>,
    layout: &str,
    n_inputs: usize,
) {
    let dir = artifacts.join(name);
    fs::create_dir_all(&dir).unwrap();

    let mut pk_bytes = Vec::new();
    pk.serialize_compressed(&mut pk_bytes).unwrap();
    fs::write(dir.join("proving_key.bin"), &pk_bytes).unwrap();

    let mut vk_bytes = Vec::new();
    vk.serialize_compressed(&mut vk_bytes).unwrap();
    fs::write(dir.join("verifying_key.bin"), &vk_bytes).unwrap();

    // Host-encoded VK: the exact bytes a zk_verifier is constructed with.
    let host_vk = vk_to_bytes(vk);
    let mut hex_out = String::new();
    writeln!(hex_out, "# {name} verifying key, Soroban host encoding (DEV)").unwrap();
    writeln!(hex_out, "alpha={}", hex::encode(host_vk.alpha)).unwrap();
    writeln!(hex_out, "beta={}", hex::encode(host_vk.beta)).unwrap();
    writeln!(hex_out, "gamma={}", hex::encode(host_vk.gamma)).unwrap();
    writeln!(hex_out, "delta={}", hex::encode(host_vk.delta)).unwrap();
    for (i, ic) in host_vk.ic.iter().enumerate() {
        writeln!(hex_out, "ic.{i}={}", hex::encode(ic)).unwrap();
    }
    fs::write(dir.join("verifying_key.hex"), hex_out).unwrap();

    let layout_txt = format!(
        "circuit={name}\ntree_depth={POOL_TREE_DEPTH}\npublic_inputs={n_inputs}\norder={layout}\nkeys=DEV (public seed)\n"
    );
    fs::write(dir.join("layout.txt"), layout_txt).unwrap();

    let sums = [
        "proving_key.bin",
        "verifying_key.bin",
        "verifying_key.hex",
        "layout.txt",
    ]
    .iter()
    .map(|f| {
        let data = fs::read(dir.join(f)).unwrap();
        format!("{}  {}\n", sha256_hex(&data), f)
    })
    .collect::<String>();
    fs::write(dir.join("SHA256SUMS"), sums).unwrap();

    assert_eq!(host_vk.ic.len(), n_inputs + 1, "IC/layout mismatch");
    println!("  {name}: vk ic={} (inputs {n_inputs})", host_vk.ic.len());
}

/// Emits the Poseidon round constants and MDS matrix as a generated
/// Rust source consumed by `contracts/shielded_pool` — the single
/// source of truth for the on-chain hash matching the circuits.
fn emit_poseidon_params(root: &Path) {
    let params = poseidon::parameters();
    let out_path = root
        .parent()
        .unwrap()
        .join("contracts/shielded_pool/src/poseidon_params.rs");

    let mut out = String::new();
    out.push_str(
        "//! GENERATED FILE — do not edit.\n\
         //!\n\
         //! Poseidon constants for the protocol hash, generated by\n\
         //! `circuits/scripts/build-artifacts.sh` from the instance\n\
         //! described in `circuits/src/poseidon.rs` (t=3, x^5, 8 full +\n\
         //! 57 partial rounds, Grain LFSR constants). Big-endian Fr\n\
         //! encodings, ready for `Bls12381Fr::from_bytes`.\n\n",
    );
    writeln!(out, "/// Permutation width.").unwrap();
    writeln!(out, "pub const WIDTH: usize = {};", poseidon::WIDTH).unwrap();
    writeln!(
        out,
        "/// Full rounds (half before, half after the partial rounds)."
    )
    .unwrap();
    writeln!(out, "pub const FULL_ROUNDS: usize = {};", poseidon::FULL_ROUNDS).unwrap();
    writeln!(out, "/// Partial rounds.").unwrap();
    writeln!(
        out,
        "pub const PARTIAL_ROUNDS: usize = {};",
        poseidon::PARTIAL_ROUNDS
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "/// Additive round constants, one row of `WIDTH` per round."
    )
    .unwrap();
    writeln!(
        out,
        "pub const ARK: [[[u8; 32]; WIDTH]; FULL_ROUNDS + PARTIAL_ROUNDS] = ["
    )
    .unwrap();
    for row in &params.ark {
        writeln!(out, "    [").unwrap();
        for c in row {
            writeln!(out, "        {},", byte_array(&fr_to_bytes(*c))).unwrap();
        }
        writeln!(out, "    ],").unwrap();
    }
    writeln!(out, "];").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "/// The `WIDTH x WIDTH` MDS matrix.").unwrap();
    writeln!(out, "pub const MDS: [[[u8; 32]; WIDTH]; WIDTH] = [").unwrap();
    for row in &params.mds {
        writeln!(out, "    [").unwrap();
        for c in row {
            writeln!(out, "        {},", byte_array(&fr_to_bytes(*c))).unwrap();
        }
        writeln!(out, "    ],").unwrap();
    }
    writeln!(out, "];").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "/// Empty-subtree hashes: `ZEROS[0] = 0`, `ZEROS[i+1] = H(z_i, z_i)`,"
    )
    .unwrap();
    writeln!(
        out,
        "/// up to the pool tree depth — saves the constructor {POOL_TREE_DEPTH} hashes."
    )
    .unwrap();
    writeln!(
        out,
        "pub const ZEROS: [[u8; 32]; {}] = [",
        POOL_TREE_DEPTH + 1
    )
    .unwrap();
    let mut z = Fr::from(0u64);
    for _ in 0..=POOL_TREE_DEPTH {
        writeln!(out, "    {},", byte_array(&fr_to_bytes(z))).unwrap();
        z = poseidon::hash2(z, z);
    }
    writeln!(out, "];").unwrap();

    fs::write(&out_path, out).unwrap();

    // Cross-check vector so the contract can pin hash2(1, 2).
    let h = poseidon::hash2(Fr::from(1u64), Fr::from(2u64));
    println!(
        "  poseidon params -> {} (hash2(1,2) = {})",
        out_path.display(),
        hex::encode(fr_to_bytes(h))
    );
}

fn byte_array(bytes: &[u8; 32]) -> String {
    let inner = bytes
        .iter()
        .map(|b| format!("{b}"))
        .collect::<Vec<_>>()
        .join(", ");
    format!("[{inner}]")
}

fn sha256_hex(data: &[u8]) -> String {
    // Tiny dependency-free SHA-256 (FIPS 180-4), used only for the
    // artifact checksum manifest.
    const K: [u32; 64] = [
        0x428a2f98, 0x71374491, 0xb5c0fbcf, 0xe9b5dba5, 0x3956c25b, 0x59f111f1, 0x923f82a4,
        0xab1c5ed5, 0xd807aa98, 0x12835b01, 0x243185be, 0x550c7dc3, 0x72be5d74, 0x80deb1fe,
        0x9bdc06a7, 0xc19bf174, 0xe49b69c1, 0xefbe4786, 0x0fc19dc6, 0x240ca1cc, 0x2de92c6f,
        0x4a7484aa, 0x5cb0a9dc, 0x76f988da, 0x983e5152, 0xa831c66d, 0xb00327c8, 0xbf597fc7,
        0xc6e00bf3, 0xd5a79147, 0x06ca6351, 0x14292967, 0x27b70a85, 0x2e1b2138, 0x4d2c6dfc,
        0x53380d13, 0x650a7354, 0x766a0abb, 0x81c2c92e, 0x92722c85, 0xa2bfe8a1, 0xa81a664b,
        0xc24b8b70, 0xc76c51a3, 0xd192e819, 0xd6990624, 0xf40e3585, 0x106aa070, 0x19a4c116,
        0x1e376c08, 0x2748774c, 0x34b0bcb5, 0x391c0cb3, 0x4ed8aa4a, 0x5b9cca4f, 0x682e6ff3,
        0x748f82ee, 0x78a5636f, 0x84c87814, 0x8cc70208, 0x90befffa, 0xa4506ceb, 0xbef9a3f7,
        0xc67178f2,
    ];
    let mut h: [u32; 8] = [
        0x6a09e667, 0xbb67ae85, 0x3c6ef372, 0xa54ff53a, 0x510e527f, 0x9b05688c, 0x1f83d9ab,
        0x5be0cd19,
    ];
    let mut msg = data.to_vec();
    let bit_len = (data.len() as u64) * 8;
    msg.push(0x80);
    while msg.len() % 64 != 56 {
        msg.push(0);
    }
    msg.extend_from_slice(&bit_len.to_be_bytes());
    for chunk in msg.chunks(64) {
        let mut w = [0u32; 64];
        for (i, word) in chunk.chunks(4).enumerate() {
            w[i] = u32::from_be_bytes(word.try_into().unwrap());
        }
        for i in 16..64 {
            let s0 = w[i - 15].rotate_right(7) ^ w[i - 15].rotate_right(18) ^ (w[i - 15] >> 3);
            let s1 = w[i - 2].rotate_right(17) ^ w[i - 2].rotate_right(19) ^ (w[i - 2] >> 10);
            w[i] = w[i - 16]
                .wrapping_add(s0)
                .wrapping_add(w[i - 7])
                .wrapping_add(s1);
        }
        let (mut a, mut b, mut c, mut d, mut e, mut f, mut g, mut hh) =
            (h[0], h[1], h[2], h[3], h[4], h[5], h[6], h[7]);
        for i in 0..64 {
            let s1 = e.rotate_right(6) ^ e.rotate_right(11) ^ e.rotate_right(25);
            let ch = (e & f) ^ (!e & g);
            let t1 = hh
                .wrapping_add(s1)
                .wrapping_add(ch)
                .wrapping_add(K[i])
                .wrapping_add(w[i]);
            let s0 = a.rotate_right(2) ^ a.rotate_right(13) ^ a.rotate_right(22);
            let maj = (a & b) ^ (a & c) ^ (b & c);
            let t2 = s0.wrapping_add(maj);
            hh = g;
            g = f;
            f = e;
            e = d.wrapping_add(t1);
            d = c;
            c = b;
            b = a;
            a = t1.wrapping_add(t2);
        }
        h[0] = h[0].wrapping_add(a);
        h[1] = h[1].wrapping_add(b);
        h[2] = h[2].wrapping_add(c);
        h[3] = h[3].wrapping_add(d);
        h[4] = h[4].wrapping_add(e);
        h[5] = h[5].wrapping_add(f);
        h[6] = h[6].wrapping_add(g);
        h[7] = h[7].wrapping_add(hh);
    }
    h.iter().map(|x| format!("{x:08x}")).collect()
}
